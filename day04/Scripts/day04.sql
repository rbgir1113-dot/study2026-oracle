-- 평균 댓글 개수보다 많이 달린 게시글
-- 평균 댓글 개수
SELECT *
FROM TBL_POST
WHERE ID IN (
   SELECT POST_ID
   FROM TBL_REPLY
   GROUP BY POST_ID
   HAVING COUNT(POST_ID) >= (
      SELECT ROUND(AVG(POST_COUNT), 1)
      FROM (
         SELECT COUNT(POST_ID) "POST_COUNT"
         FROM TBL_REPLY
         GROUP BY POST_ID
      )
   )
);

-- 가장 댓글이 많이 달린 게시글을 작성한 사용자
