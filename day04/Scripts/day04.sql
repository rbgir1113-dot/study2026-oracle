-- 재고가 10개이면서, 판매 가격중에서 2000원이 초과하는 상품들 조회
SELECT TBP.*
FROM (
	SELECT *
	FROM TBL_PRODUCT
	WHERE PRODUCT_STOCK = 10
) TBP
WHERE TBP.PRODUCT_PRICE > 2000;

-- 재고가 20, 30, 40인 상품들의 ID들을 조회
SELECT TBP.ID
FROM (
	SELECT *
	FROM TBL_PRODUCT
	WHERE PRODUCT_STOCK IN (20,30,40)
) TBP;

-- 가장 비싼 상품 조회
SELECT *
FROM TBL_PRODUCT
WHERE PRODUCT_PRICE = (
	SELECT MAX(PRODUCT_PRICE)
	FROM TBL_PRODUCT
);

-- 판매 상품의 평균 가격보다 비싼 상품들 조회
SELECT *
FROM TBL_PRODUCT
WHERE PRODUCT_PRICE >= (
	SELECT AVG(PRODUCT_PRICE)
	FROM TBL_PRODUCT
);

