-- 가장 나이가 어린 사용자가 작성한 게시글 조회
-- 1.
SELECT TBP.POST_TITLE , TBP.POST_CONTENT 
FROM TBL_POST TBP
JOIN (
	SELECT ID 
	FROM TBL_USER
	WHERE USER_BIRTH = (
		SELECT MAX(USER_BIRTH)
		FROM TBL_USER
	)
) TBU ON TBP.USER_ID = TBU.ID;

-- 2.
SELECT TBP.*
FROM TBL_POST TBP
JOIN (
   SELECT *
   FROM TBL_USER
   ORDER BY USER_BIRTH DESC
   FETCH FIRST 1 ROWS ONLY
) TBU
ON TBP.USER_ID = TBU.ID;

-- 3.
SELECT *
FROM (
   SELECT *
   FROM TBL_USER
   ORDER BY USER_BIRTH DESC
)
WHERE ROWNUM = 1;
