SELECT DISTINCT TBU.USER_EMAIL , TBU.USER_ADDRESS 
FROM (
	SELECT POST_ID
	FROM TBL_REPLY
	GROUP BY POST_ID
	HAVING COUNT(POST_ID) >= 2
) TBR
JOIN TBL_POST TBP ON TBR.POST_ID = TBP.ID
JOIN TBL_USER TBU ON TBP.USER_ID = TBU.ID

