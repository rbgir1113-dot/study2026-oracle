-- 평균 댓글 개수보다 작게 달린 게시글을 작성한 유저 조회
SELECT TBU.*
FROM (
	SELECT POST_ID
	FROM TBL_REPLY
	GROUP BY POST_ID 
	HAVING COUNT(POST_ID) < (
		SELECT AVG(COUNT(POST_ID))
		FROM TBL_REPLY
		GROUP BY POST_ID
	)
) TBR
JOIN TBL_POST TBP ON TBR.POST_ID = TBP.ID
JOIN TBL_USER TBU ON TBP.USER_ID = TBU.ID;
